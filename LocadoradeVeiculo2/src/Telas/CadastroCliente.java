    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int i = Tabela.getSelectedRow();
        if(i >= 0 && i < Clientes.size()){
            Cliente clt = Clientes.get(i);
            Clientes.remove(i);
            if(ChamadoAtivo){
            chm.excluirTxtId(clt);
            }
            for(int j = 0; j < Chamados.size();j++){
            if(Chamados.get(j).getClt().getId() == clt.getId()){
                Chamados.remove(j);
            }
        }
            
        }
        else if(indexAux >= 0 && indexAux < Clientes.size()){
            Cliente clt = Clientes.get(indexAux);
            Clientes.remove(indexAux);
            if(ChamadoAtivo){
            chm.excluirTxtId(clt);
            }
            indexAux = -1;
           for(int j = 0; j < Chamados.size();j++){
            if(Chamados.get(j).getClt().getId() == clt.getId()){
                Chamados.remove(j);
            }
        }
        }

        txtCPF.setText("");
        txtId.setText("");
        txtNome.setText("");
        txtRG.setText("");
        txtTelefone.setText("");
       
        btnCadastrar.setEnabled(true);
        btnCancelar.setEnabled(false);
        btnEditar.setEnabled(false);
        btnExcluir.setEnabled(false);
        btnOK.setEnabled(false);
        btnSalvar.setEnabled(false);
        btnPesquisar.setEnabled(true);
       
        txtCPF.setEnabled(false);
        txtId.setEnabled(false);
        txtNome.setEnabled(false);
        txtRG.setEnabled(false);
        txtTelefone.setEnabled(false);
        
        carregarTabela();
        if(ChamadoAtivo){
            chm.carregarTabela2();
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

