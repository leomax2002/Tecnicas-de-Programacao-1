    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        boolean flagAux;
        try{
            Integer.parseInt(txtId.getText());
            flagAux = true;
        }
        catch(NumberFormatException e){
            flagAux = false;
        }
        if(txtCPF.getText().equals("")||txtId.getText().equals("")||txtNome.getText().equals("")||txtRG.getText().equals("")||txtTelefone.getText().equals("") || !flagAux){
            JOptionPane.showMessageDialog(null, "Campos não Preenchidos ou Preenchidos Incorretamente","Mensagem", JOptionPane.PLAIN_MESSAGE);
            txtId.requestFocus();
        }
        else{
        String cpf = txtCPF.getText();
        int id = Integer.parseInt(txtId.getText());
        String nome = txtNome.getText();
        String rg = txtRG.getText();
        String telefone = txtTelefone.getText();
        
        if(btn.equals("novo")){
        Cliente clt = new Cliente(id,rg,nome,telefone,cpf);
        Clientes.add(clt);
        JOptionPane.showMessageDialog(null, "Cliente Cadastrado","Mensagem", JOptionPane.PLAIN_MESSAGE);
        }
        else if(btn.equals("editar")){
            int ind = Tabela.getSelectedRow();
            if(ind != -1){
                
                if(ChamadoAtivo){
                chm.excluirTxtId(Clientes.get(ind));
                }
                Clientes.get(ind).setId(id);
                Clientes.get(ind).setRG(rg);
                Clientes.get(ind).setNome(nome);
                Clientes.get(ind).setTelefone(telefone);
                Clientes.get(ind).setCPF(cpf);
                
                if(ChamadoAtivo){
                chm.adicionarTxtId(Clientes.get(ind));
                }
            }
            else{
                if(ChamadoAtivo){
                Cliente clt = Clientes.get(indexAux);
                chm.excluirTxtId(clt);
                }
                Clientes.get(indexAux).setId(id);
                Clientes.get(indexAux).setRG(rg);
                Clientes.get(indexAux).setNome(nome);
                Clientes.get(indexAux).setTelefone(telefone);
                Clientes.get(indexAux).setCPF(cpf);
                if(ChamadoAtivo){
                chm.adicionarTxtId(Clientes.get(indexAux));
                }
                
                indexAux = -1;
            }
            
        }
        txtCPF.setText("");
        txtId.setText("");
        txtNome.setText("");
        txtRG.setText("");
        txtTelefone.setText("");
       
        btnCadastrar.setEnabled(true);
        btnCancelar.setEnabled(false);
        btnEditar.setEnabled(false);
        btnExcluir.setEnabled(false);
        btnOK.setEnabled(false);
        btnSalvar.setEnabled(false);
        btnPesquisar.setEnabled(true);
       
        txtCPF.setEnabled(false);
        txtId.setEnabled(false);
        txtNome.setEnabled(false);
        txtRG.setEnabled(false);
        txtTelefone.setEnabled(false);
        
        carregarTabela();
        if(ChamadoAtivo){
            chm.carregarTabela2();
        }

        }
    }//GEN-LAST:event_btnSalvarActionPerformed

