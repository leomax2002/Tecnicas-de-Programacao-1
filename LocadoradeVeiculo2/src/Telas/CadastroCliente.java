    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        String idStr = "";
        String rgStr = "";
        String nomeStr = "";
        String telefoneStr = "";
        String cpfStr = "";
        if(txtCPF.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Informe o CPF do Cliente","Mensagem", JOptionPane.PLAIN_MESSAGE);
        }
        else{
            Cliente client;
            String cpf = txtCPF.getText();
            
            for(int i = 0; i < Clientes.size(); i++){
                client = Clientes.get(i);
                
                if(cpf.equals(client.getCPF()) ){
                    indexAux = i;
                    idStr = String.valueOf(client.getId());
                    rgStr =client.getRG();
                    nomeStr =client.getNome();
                    telefoneStr = client.getTelefone();
                    cpfStr = client.getCPF();
                    
                }
            }
            if(cpfStr.equals("")){
                JOptionPane.showMessageDialog(null, "CPF inexistente","Mensagem", JOptionPane.PLAIN_MESSAGE);
            }
            else{
                txtId.setText(idStr);
                txtRG.setText(rgStr);
                txtNome.setText(nomeStr);
                txtTelefone.setText(telefoneStr);
                txtCPF.setText(cpfStr);
                
                btnCadastrar.setEnabled(false);
                btnCancelar.setEnabled(false);
                btnEditar.setEnabled(true);
                btnExcluir.setEnabled(true);
                btnOK.setEnabled(false);
                btnSalvar.setEnabled(false);
                btnPesquisar.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btnOKActionPerformed

