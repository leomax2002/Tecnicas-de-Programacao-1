    private void btnOkTagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkTagActionPerformed
        boolean tagNaoExiste = true;
        for(int i = 0; i < tag.size();i++){
            if(tag.get(i).equals(txtTag.getText())){
                tagNaoExiste = false;
                break;
            }
        }
        if(tagNaoExiste){
            tag.add(txtTag.getText());
        }
        txtTag.setText("");
        atualizalstTags();
    }//GEN-LAST:event_btnOkTagActionPerformed

