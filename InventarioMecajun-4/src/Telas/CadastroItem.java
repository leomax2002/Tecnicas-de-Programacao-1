    public void atualizaboxListaProjetos(){
        cbItem.removeAllItems();
        if(listaProjetos != null){
        for(int i = 0; i < listaProjetos.size();i++){
          Projeto projetoAux = listaProjetos.get(i);
          cbItem.addItem(projetoAux.getNome());
        }
      }
    }
