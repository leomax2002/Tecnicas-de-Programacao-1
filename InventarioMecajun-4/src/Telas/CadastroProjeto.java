    public void atualizalstMembrosProj(){
        if(listaMembros != null){            
            String[] membrosAux = new String[listaMembros.size()];
            for(int i = 0; i < listaMembros.size();i++){
                membrosAux[i] = listaMembros.get(i).getNomeMembro();
            }
            lstMembrosProj.setModel(new DefaultComboBoxModel<>(membrosAux));
        }
        else{
            String[] vazio = {"","","","",""};
            lstMembrosProj.setModel(new DefaultComboBoxModel<>(vazio));
        }
        lstMembrosProj.setSelectedIndex(-1);
    }
