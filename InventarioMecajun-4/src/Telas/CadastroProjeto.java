    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        TxtNome.setEnabled(true);
        TxtDescricao.setEnabled(true);
        lstMembrosProj.setEnabled(false);
        
        String nom = TxtNome.getText();
        String desc = TxtDescricao.getText();
        ArrayList<Membro> membrosProj = new ArrayList<Membro>();
        int[] indMembros = lstMembrosProj.getSelectedIndices();
        for(int i = 0; i < indMembros.length;i++){
            membrosProj.add(listaMembros.get(indMembros[i]));
        }
              
        Projeto projeto = new Projeto(nom,desc,membrosProj);
        for(int i = 0; i < listaProjetos.size();i++){
            if(listaProjetos.get(i).getNome().equals(nom)){
                listaProjetos.remove(i);
            }
        }
        listaProjetos.add(projeto);
        atualizalstMembrosProj();
        TxtNome.setText("");
        TxtDescricao.setText("");
        lstMembrosProj.setSelectedIndex(-1);
        BtnSalvar.setEnabled(false);
        BtnNovo.setEnabled(true);
        BtnPesquisar.setEnabled(true);
        BtnEditar.setEnabled(false);
        BtnExcluir.setEnabled(false);
        
        JOptionPane.showMessageDialog(null, "Projeto cadastrado", "Aviso", JOptionPane.INFORMATION_MESSAGE);               
        
    }//GEN-LAST:event_BtnSalvarActionPerformed

    private void BtnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPesquisarActionPerformed
        BtnEditar.setEnabled(true);
        BtnSalvar.setEnabled(false);
        BtnNovo.setEnabled(true);
        BtnPesquisar.setEnabled(true);
        BtnExcluir.setEnabled(true);
        
        TxtNome.setEnabled(true);
        TxtDescricao.setEnabled(false);
        lstMembrosProj.setEnabled(false);
        
        if (listaProjetos.isEmpty()){
            JOptionPane.showMessageDialog(null, "Nenhum projeto está cadastrado.", "Aviso", JOptionPane.PLAIN_MESSAGE); 
            TxtNome.setText("");
            TxtDescricao.setText("");
            atualizalstMembrosProj();
        }
        if(TxtNome.getText().equals("")){
            JOptionPane.showMessageDialog(null,"O nome ser informado","Aviso",JOptionPane.PLAIN_MESSAGE);
        }
        else{
            
            Projeto proj;
            String nome = "", descricao = "";
            String nom = TxtNome.getText();
            String[] membrosProjPesq = {""};
            for (int i = 0; i < listaProjetos.size(); i++){
                proj = listaProjetos.get(i);
                if (nom == null ? proj.getNome()== null : nom.equals(proj.getNome())){
                    nome = String.valueOf(proj.getNome());
                    descricao = String.valueOf(proj.getDescricao());
                    atualizalstMembrosProj();
                }
            }
            if (nome.equals("")){
                JOptionPane.showMessageDialog(null,"Nenhum projeto com este nome encontrado","Aviso",JOptionPane.PLAIN_MESSAGE);
                TxtNome.setText("");
                TxtDescricao.setText("");
                lstMembrosProj.setModel(new DefaultComboBoxModel<String>(membrosProjPesq));
            }else{
                TxtNome.setText(nome);
                TxtDescricao.setText(descricao);
                atualizalstMembrosProj();
                
            }

        }

            // TODO add your handling code here:
    }//GEN-LAST:event_BtnPesquisarActionPerformed

